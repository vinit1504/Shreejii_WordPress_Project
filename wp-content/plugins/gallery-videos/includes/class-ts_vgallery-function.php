	public function tsvg_icon_swap( $key, $value ) {
		if ( $key != 'ts_vgallery_ch_tbc' && $key != 'ts_vgallery_ch_tac' ) {
			if ( strlen( $value ) == 4 ) {
				if ( $value == 'none' || $value == 'None' ) {
					return 'ts-vgallery ts-vgallery-none';
				} else {
					if ( array_key_exists( $value, $this->ts_vgallery_fonts_array ) ) {
						return $this->ts_vgallery_fonts_array[ $value ];
					} else {
						return 'ts-vgallery ts-vgallery-none';
					}
				}
			} elseif ( $value == '' ) {
				return 'ts-vgallery ts-vgallery-none';
			} else {
				return $value;
			}
		} else {
			if ( strlen( $value ) != 4 ) {
				if ( in_array( $value, $this->ts_vgallery_fonts_array ) ) {
					$key = array_search( $value, $this->ts_vgallery_fonts_array );
					return $key;
				} else {
					if ( $key == 'ts_vgallery_ch_tbc' ) {
						return 'f10c';
					} else {
						return 'f192';
					}
				}
			} else {
				return $value;
			}
		}
	}
